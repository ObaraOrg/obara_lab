def main(p: int, z: int, file_name: str = "clad.inp") -> None:
    input = ""

    for p_itter in range(1, p + 1):
        for z_itter in range(1, z + 1):
            zone_template = f"{clad_template(p_itter, z_itter)}\n"
            input += zone_template

    with open(file_name, "w") as f:
        f.write(input)


if __name__ == "__main__":
    main()
